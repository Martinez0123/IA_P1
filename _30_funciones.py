#con la funcion def insertamos la variable suma de la funcion 
def suma(numero1, numero2):
    resultado = numero1 + numero2
    print(f"El resultado de la suma es: {resultado}")

# Llamadas a la función para obtener los resultados especificados
print("Primera llamada:")
suma(10, 20)  # Resultado: 30

print("\nSegunda llamada:")
suma(30, 20)  # Resultado: 50

print("\nTercera llamada:")
suma(55000, 2000)  # Resultado: 57000
