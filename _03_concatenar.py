# Se declara la variable nombre con una cadena de texto que contiene "Jorge Alejandro"
nombre = "Jorge Alejandro"

# Se declara la variable apellido con una cadena de texto que contiene "Martinez Alvarez Del Castillo"
apellido = "Martinez Alvarez Del Castillo"

# Se concatenan las variables nombre y apellido con un espacio intermedio y un punto al final.
# El resultado se guarda en la variable nombre_completo
nombre_completo = nombre + " " + apellido + "."

# Se imprime el valor de la variable nombre_completo en la consola
print(nombre_completo)
