# Se declara una lista llamada frutas que contiene varios nombres de frutas.
frutas = ['manzana', 'fresa', 'melon', 'platano', 'guayaba', 'uva', 'sandia', 'tuna']

# Se ordena la lista de frutas en orden alfabético de forma descendente (de la Z a la A)
frutas.sort(reverse=True)

# Se imprime la lista resultante después de ordenarla
print(frutas)
