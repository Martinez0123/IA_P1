# Se declara una variable llamada color y se le asigna el valor 'rojo'
color = "rojo"

# Se utiliza una estructura condicional if para verificar si el color es 'rojo'
if color == "rojo":
    # Si el color es 'rojo', se ejecutará este bloque
    print("El color es rojo.")
else:
    # Si el color no es 'rojo', se ejecutará este bloque
    print("El color no es rojo.")
