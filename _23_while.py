# Inicialización de una variable para usar en el bucle
numero = 0

# Bucle con incrementos
print("Bucle con incrementos:")
while numero < 5:
    print("Número actual:", numero)
    numero += 1  # Incremento de la variable

# Reiniciar la variable para el bucle de decrementos
numero = 5

# Bucle con decrementos
print("\nBucle con decrementos:")
while numero > 0:
    print("Número actual:", numero)
    numero -= 1  # Decremento de la variable
